
def area(a):
    '''
    Возвращает площадь квадрата.
        Параметры:
        a (float) - сторона квадрата

        Возвращаемое значение:
        Произведение стороны на саму себя
    '''
    return a * a


def perimeter(a):
    '''
    Возвращает периметр квадрата.
        Параметры:
        a (float) - сторона квадрата

        Возвращаемое значение:
        Произведение стороны a на 4
    '''
    return 4 * a
